/**
 * Get a random quiz of 10 questions, balanced across categories and with randomized options
 */
export function getRandomQuiz(count = 10) {
  const shuffled = [...quizQuestions].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, count);
  
  // Randomize the options for each question to avoid predictable answers
  return selected.map(q => {
    const optionsWithIndex = q.options.map((opt, i) => ({ text: opt, isCorrect: i === q.correct }));
    const shuffledOptions = optionsWithIndex.sort(() => Math.random() - 0.5);
    const correctIndex = shuffledOptions.findIndex(o => o.isCorrect);
    
    return {
      ...q,
      options: shuffledOptions.map(o => o.text),
      correct: correctIndex
    };
  });
}

/**
 * Get quiz by difficulty
 */
export function getQuizByDifficulty(difficulty, count = 10) {
  const filtered = quizQuestions.filter(q => q.difficulty === difficulty);
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export default quizQuestions;
